package ticket;

public class RoomEXVO {

	private int r_no;
	private String room_no;
	
	public RoomEXVO() {

	}

	public RoomEXVO(int r_no, String room_no) {

		this.r_no = r_no;
		this.room_no = room_no;
	}

	public int getR_no() {
		return r_no;
	}

	public void setR_no(int r_no) {
		this.r_no = r_no;
	}

	public String getRoom_no() {
		return room_no;
	}

	public void setRoom_no(String room_no) {
		this.room_no = room_no;
	}
	
	
}
